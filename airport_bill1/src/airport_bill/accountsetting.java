    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String sql = "update accout set c_name=?,c_surname=?,c_telephone=?,c_username=?,c_password=?,gender=? where c_id=?";
            PreparedStatement pst = c.prepareStatement(sql);
            pst.clearParameters();
            pst.setString(1, txtname.getText());
            pst.setString(2, txtsurname.getText());

            pst.setString(3, txtphone.getText());
            pst.setString(4, txtusername.getText());
            pst.setString(5, passwordtf.getText());
            pst.setString(6, jComboBox1.getSelectedItem().toString());
            pst.setString(7, id);

            if (pst.executeUpdate() == 1) {

                JOptionPane.showMessageDialog(null, "change done");
                Home h = new Home(id);
                h.show();
                dispose();

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

