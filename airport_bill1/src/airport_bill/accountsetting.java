    public void setting() {
        try {
            String sql = "select*from accout where c_id=?";
            PreparedStatement pst = c.prepareStatement(sql);
            pst.clearParameters();
            pst.setString(1, id);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                txtname.setText(rs.getString("c_name"));
                txtsurname.setText(rs.getString("C_surname"));
                txtphone.setText(rs.getString("c_telephone"));
                passwordtf.setText(rs.getString("c_password"));
                txtusername.setText(rs.getString("c_username"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

